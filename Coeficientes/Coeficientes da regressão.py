X = []
y = []

for ini, meio, fim in dados_completos:  # só dados com meio conhecido
    if fim == ini:
        print(f"⚠️ Ignorando intervalo com fim == ini ({ini}) para regressão")
        continue  # pular este caso
    
    ini_int = int(ini)
    fim_int = int(fim)

    log2_ini = ini_int.bit_length() - 1 + np.log2(ini_int / 2**(ini_int.bit_length() - 1))
    log2_fim = fim_int.bit_length() - 1 + np.log2(fim_int / 2**(fim_int.bit_length() - 1))

    rel = float((meio - ini) / (fim - ini))
    X.append([
        log2_ini,
        log2_fim,
        log2_ini**2,
        log2_fim**2,
        log2_ini * log2_fim,
        1
    ])
    y.append(rel)


X = np.array(X)
y = np.array(y)

coef, *_ = np.linalg.lstsq(X, y, rcond=None)

