for inicio_str, meio_str, fim_str in dados:
    inicio = Decimal(inicio_str)
    fim = Decimal(fim_str)
    intervalo = fim - inicio

    if meio_str is not None and meio_str.lower() != 'none':
        meio = Decimal(meio_str)

        if intervalo == 0:
            passos_ate_meio = Decimal(0)
            passos_meio_ate_fim = Decimal(0)
            meio_calculado_1 = inicio
            meio_calculado_2 = fim
        else:
            proporcao_meio = (meio - inicio) / intervalo
            proporcao_fim = (fim - meio) / intervalo

            passos_ate_meio = proporcao_meio * Decimal(255)
            passos_meio_ate_fim = proporcao_fim * Decimal(255)

            meio_calculado_1 = inicio + (passos_ate_meio / Decimal(255)) * intervalo
            meio_calculado_2 = fim - (passos_meio_ate_fim / Decimal(255)) * intervalo

        diff_1 = meio_calculado_1 - meio
        diff_2 = meio_calculado_2 - meio
        diff_3 = meio_calculado_2 - meio_calculado_1

        linhas.append([
            f"'{inicio}",
            f"'{meio}",
            f"'{fim}",
            f"'{formatar_decimal(passos_ate_meio)}",
            f"'{formatar_decimal(passos_meio_ate_fim)}",
            f"'{formatar_decimal(meio_calculado_1)}",
            f"'{formatar_decimal(meio_calculado_2)}",
            f"'{formatar_decimal(diff_1)}",
            f"'{formatar_decimal(diff_2)}",
            f"'{formatar_decimal(diff_3)}"
        ])
